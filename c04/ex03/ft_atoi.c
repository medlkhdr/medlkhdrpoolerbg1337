int	ft_atoi(char *str)
{
	int	i;
	int	n;
	int	digits;

	i = 0;
	n = 1;
	digits = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n = -n;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		digits = digits * 10 + (str[i] - '0');
		i++;
	}
	return (digits * n);
}
